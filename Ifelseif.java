
public class Ifelseif {

	public static void main(String[] args) {
		
		int n = 4;
		if(n%2==0) 
			System.out.println("Even Number");
		else
		System.out.println("Odd Number");
		
		
		int a = 8,b=7,c=9;
		
		if(a>b && a>c)
			System.out.println(a);
		else if(b>c)
			System.out.println(b);
		else
			System.out.println(c);
	}

}
